def get_knowledge_base():
    """获取完整知识库"""
    return KNOWLEDGE_BASE_DOCS


def get_categories():
    """获取所有分类"""
    categories = set()
    for doc in KNOWLEDGE_BASE_DOCS:
        categories.add(doc["category"])
    return sorted(list(categories))


def get_sources():
    """获取所有来源"""
    sources = {}
    for doc in KNOWLEDGE_BASE_DOCS:
        source = doc["source"]
        if source not in sources:
            sources[source] = {
                "count": 0,
                "type": doc["source_type"],
                "url": doc.get("source_url", "")
            }
        sources[source]["count"] += 1
    return sources


def get_docs_by_category(category):
    """按分类获取文档"""
    return [doc for doc in KNOWLEDGE_BASE_DOCS if doc["category"] == category]


